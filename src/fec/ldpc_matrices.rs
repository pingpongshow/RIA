//! IEEE 802.11n QC-LDPC Matrices
//!
//! This module contains the base matrices (Hbm) for IEEE 802.11n LDPC codes.
//! These are quasi-cyclic LDPC codes with three block sizes (n) and four code rates.
//!
//! Block sizes: 648 (Z=27), 1296 (Z=54), 1944 (Z=81)
//! Code rates: 1/2, 2/3, 3/4, 5/6
//!
//! The base matrix entries indicate cyclic shift values for ZxZ identity matrices.
//! A value of -1 indicates a zero matrix.

use crate::fec::CodeRate;

/// Expansion factor for each block size
pub const Z_648: usize = 27;
pub const Z_1296: usize = 54;
pub const Z_1944: usize = 81;

/// Block sizes
pub const BLOCK_648: usize = 648;
pub const BLOCK_1296: usize = 1296;
pub const BLOCK_1944: usize = 1944;

/// Get expansion factor for block size
pub fn expansion_factor(block_size: usize) -> usize {
    match block_size {
        BLOCK_648 => Z_648,
        BLOCK_1296 => Z_1296,
        BLOCK_1944 => Z_1944,
        _ => panic!("Invalid LDPC block size: {}", block_size),
    }
}

/// Select appropriate LDPC block size for given input bits
pub fn select_block_size(input_bits: usize) -> usize {
    if input_bits <= 324 {
        BLOCK_648
    } else if input_bits <= 648 {
        BLOCK_1296
    } else {
        BLOCK_1944
    }
}

/// Information bits (k) for each block size and rate
pub fn info_bits(block_size: usize, rate: CodeRate) -> usize {
    match rate {
        CodeRate::Rate1_2 => block_size / 2,
        CodeRate::Rate2_3 => block_size * 2 / 3,
        CodeRate::Rate3_4 => block_size * 3 / 4,
        CodeRate::Rate5_6 => block_size * 5 / 6,
        CodeRate::Rate1_3 => block_size / 3, // Not standard 802.11n, but supported
    }
}

/// Parity bits (m) for each block size and rate
pub fn parity_bits(block_size: usize, rate: CodeRate) -> usize {
    block_size - info_bits(block_size, rate)
}

/// Number of rows in base matrix (mb)
pub fn base_matrix_rows(rate: CodeRate) -> usize {
    match rate {
        CodeRate::Rate1_2 => 12,
        CodeRate::Rate2_3 => 8,
        CodeRate::Rate3_4 => 6,
        CodeRate::Rate5_6 => 4,
        CodeRate::Rate1_3 => 16, // Extended, not standard
    }
}

/// Number of columns in base matrix (nb)
pub fn base_matrix_cols(_rate: CodeRate) -> usize {
    24 // All 802.11n codes have 24 columns (n/Z = 24)
}

/// Get the base matrix for a given block size and rate
/// Returns (rows, cols, matrix_data)
/// Matrix values: -1 = zero block, 0..Z-1 = cyclic shift of identity
pub fn get_base_matrix(block_size: usize, rate: CodeRate) -> (usize, usize, &'static [i16]) {
    let rows = base_matrix_rows(rate);
    let cols = base_matrix_cols(rate);

    let matrix = match (block_size, rate) {
        // Block size 648 (Z=27)
        (BLOCK_648, CodeRate::Rate1_2) => &H_648_R12[..],
        (BLOCK_648, CodeRate::Rate2_3) => &H_648_R23[..],
        (BLOCK_648, CodeRate::Rate3_4) => &H_648_R34[..],
        (BLOCK_648, CodeRate::Rate5_6) => &H_648_R56[..],

        // Block size 1296 (Z=54)
        (BLOCK_1296, CodeRate::Rate1_2) => &H_1296_R12[..],
        (BLOCK_1296, CodeRate::Rate2_3) => &H_1296_R23[..],
        (BLOCK_1296, CodeRate::Rate3_4) => &H_1296_R34[..],
        (BLOCK_1296, CodeRate::Rate5_6) => &H_1296_R56[..],

        // Block size 1944 (Z=81)
        (BLOCK_1944, CodeRate::Rate1_2) => &H_1944_R12[..],
        (BLOCK_1944, CodeRate::Rate2_3) => &H_1944_R23[..],
        (BLOCK_1944, CodeRate::Rate3_4) => &H_1944_R34[..],
        (BLOCK_1944, CodeRate::Rate5_6) => &H_1944_R56[..],

        // Rate 1/3 not standard - use rate 1/2 as fallback
        (_, CodeRate::Rate1_3) => {
            return get_base_matrix(block_size, CodeRate::Rate1_2);
        }

        _ => panic!("Invalid LDPC parameters: block_size={}, rate={:?}", block_size, rate),
    };

    (rows, cols, matrix)
}

